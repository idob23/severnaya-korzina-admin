  /// –ü–æ–ª—É—á–∏—Ç—å –∫–ª—é—á–µ–≤—ã–µ —Å–ª–æ–≤–∞ –¥–ª—è –∫–æ–Ω–∫—Ä–µ—Ç–Ω–æ–π —É–ø—Ä–æ—â—ë–Ω–Ω–æ–π –∫–∞—Ç–µ–≥–æ—Ä–∏–∏
  static List<String>? getKeywordsForCategory(String simplifiedCategory) {
    return _categoryKeywords[simplifiedCategory];
  }

  /// –ü—Ä–æ–≤–µ—Ä–∏—Ç—å, —Å–æ–¥–µ—Ä–∂–∏—Ç –ª–∏ –Ω–∞–∑–≤–∞–Ω–∏–µ –∫–∞—Ç–µ–≥–æ—Ä–∏–∏ –∏–∑ –ø—Ä–∞–π—Å–∞
  /// –∫–∞–∫–æ–µ-–ª–∏–±–æ –∫–ª—é—á–µ–≤–æ–µ —Å–ª–æ–≤–æ
  static bool hasAnyKeyword(String originalCategory) {
    return mapToSimplifiedCategory(originalCategory) != null;
  }

  /// –ü–æ–ª—É—á–∏—Ç—å —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫—É –ø–æ –º–∞–ø–ø–∏–Ω–≥—É –∫–∞—Ç–µ–≥–æ—Ä–∏–π
  ///
  /// –ü—Ä–∏–Ω–∏–º–∞–µ—Ç —Å–ø–∏—Å–æ–∫ –æ—Ä–∏–≥–∏–Ω–∞–ª—å–Ω—ã—Ö –∫–∞—Ç–µ–≥–æ—Ä–∏–π –∏–∑ –ø—Ä–∞–π—Å–∞
  /// –∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫—É
  static Map<String, dynamic> getMappingStats(List<String> originalCategories) {
    final stats = <String, dynamic>{
      'total': originalCategories.length,
      'mapped': 0,
      'unmapped': 0,
      'distribution': <String, int>{},
      'unmappedList': <String>[],
    };

    for (var originalCat in originalCategories) {
      final simplified = mapToSimplifiedCategory(originalCat);

      if (simplified != null) {
        stats['mapped']++;

        final distribution = stats['distribution'] as Map<String, int>;
        distribution[simplified] = (distribution[simplified] ?? 0) + 1;
      } else {
        stats['unmapped']++;
        (stats['unmappedList'] as List<String>).add(originalCat);
      }
    }

    return stats;
  }
