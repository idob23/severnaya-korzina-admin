      // –ü—Ä–æ–≤–µ—Ä—è–µ–º —Å–æ–≤–ø–∞–¥–µ–Ω–∏–µ –ø–æ –ª—é–±–æ–º—É –∫–ª—é—á–µ–≤–æ–º—É —Å–ª–æ–≤—É
      for (var keyword in keywords) {
        if (normalizedInput.contains(keyword)) {
          return simplifiedCategory;
