    final stats = <String, dynamic>{
      'total': originalCategories.length,
      'mapped': 0,
      'unmapped': 0,
      'distribution': <String, int>{},
      'unmappedList': <String>[],
    };

    for (var originalCat in originalCategories) {
      final simplified = mapToSimplifiedCategory(originalCat);

      if (simplified != null) {
        stats['mapped']++;

        final distribution = stats['distribution'] as Map<String, int>;
        distribution[simplified] = (distribution[simplified] ?? 0) + 1;
      } else {
        stats['unmapped']++;
        (stats['unmappedList'] as List<String>).add(originalCat);
      }
    }

    return stats;
  }
