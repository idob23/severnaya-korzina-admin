    // –ü—Ä–æ—Ö–æ–¥–∏–º –ø–æ –≤—Å–µ–º —É–ø—Ä–æ—â—ë–Ω–Ω—ã–º –∫–∞—Ç–µ–≥–æ—Ä–∏—è–º
    for (var entry in _categoryKeywords.entries) {
      final simplifiedCategory = entry.key;
